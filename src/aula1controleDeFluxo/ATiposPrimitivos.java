package aula1controleDeFluxo;

public class ATiposPrimitivos {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, long, boolean
        byte b1 = 127; // 1 byte
        short s1 = 32767; // 2 bytes
        int num1 = 500; // 4 bytes
        long l1 = 1256L; // 8 bytes
        float num2 = 3.15f; // 4 bytes
        double num3 = 2.563985; // 8 bytes

        boolean condicao = true; // 1 bit (true or false)

        char caractere = 'R'; // 2 bytes

        //String não é tipo primitivo. É uma classe.
        String nome = "Ishigo";

    }
}